            conect = DriverManager.getConnection("jdbc:oracle:thin:@localhost:1521:XE", "isw", "isw");
            Statement st = conect.createStatement();
            String sql="SELECT ID, NOMBRE, APELLIDOP, FECHA, HORA FROM INQUILINOS_ENTRADAS WHERE FECHA = TO_CHAR(FECHA, 'DD/MM/YYYY') ORDER BY FECHA DESC";
