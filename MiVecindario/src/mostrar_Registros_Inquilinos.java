    private void buscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buscarActionPerformed
        try{
            if(n_buscar.getText().matches("^[0-9]*$")){
                if(n_buscar.getText().equals("")){
                    filtro = new TableRowSorter(datosInquilinos.getModel());
                    datosInquilinos.setRowSorter(filtro);
                    filtro.setRowFilter(RowFilter.regexFilter(n_buscar.getText(), 0));
                }
                else if(buscar_user() == 1){
                    filtro = new TableRowSorter(datosInquilinos.getModel());
                    datosInquilinos.setRowSorter(filtro);
                    filtro.setRowFilter(RowFilter.regexFilter(n_buscar.getText(), 0));
                }else{
                    JOptionPane.showMessageDialog(null, "No existe el usuario", null, JOptionPane.ERROR_MESSAGE);
                    n_buscar.setText("");
                    n_buscar.requestFocus();
                }
            }else if(n_buscar.getText().matches("^[a-zA-Z]*$")){
                if(n_buscar.getText().equals("")){
                    filtro = new TableRowSorter(datosInquilinos.getModel());
                    datosInquilinos.setRowSorter(filtro);
                    filtro.setRowFilter(RowFilter.regexFilter(n_buscar.getText(), 0));
                }
                else if(buscar_nombre() == 1){
                    filtro = new TableRowSorter(datosInquilinos.getModel());
                    datosInquilinos.setRowSorter(filtro);
                    filtro.setRowFilter(RowFilter.regexFilter(n_buscar.getText(), 1));
                }else{
                    JOptionPane.showMessageDialog(null, "No existe el usuario o esta mal escrito", null, JOptionPane.ERROR_MESSAGE);
                    n_buscar.setText("");
                    n_buscar.requestFocus();
                }
            }else{
                JOptionPane.showMessageDialog(null, "Solo se aceptan nÃºmeros o letras", null, JOptionPane.WARNING_MESSAGE);
                n_buscar.setText("");
                n_buscar.requestFocus();
            }
        }catch (Exception e){
            
        }
    }//GEN-LAST:event_buscarActionPerformed

