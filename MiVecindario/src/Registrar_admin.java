    private void registrar_administradorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_registrar_administradorActionPerformed
        try{
            if(!id_admin.getText().isEmpty() && !nombre_admin.getText().isEmpty() && !apellidoP_admin.getText().isEmpty() && !apellidoM_admin.getText().isEmpty() && !edad_admin.getText().isEmpty()
               && !telefono_admin.getText().isEmpty() && !ocup_admin.getText().isEmpty() && !veh_admin.getText().isEmpty() && 
                    !calle_admin.getText().isEmpty() && !casaNo_admin.getText().isEmpty() && !pass_admin.getText().isEmpty()){
                if(opc_hombre.isSelected() && !opc_mujer.isSelected()){
                    genero = "H";
                    if(registrar()==1){
                        JOptionPane.showMessageDialog(null, "Registrado");
                    }else{
                        JOptionPane.showMessageDialog(null, "No Registrado");
                    }
                }else if(!opc_hombre.isSelected() && opc_mujer.isSelected()){
                    genero = "M";
                    if(registrar()==1){
                        JOptionPane.showMessageDialog(null, "Registrado");
                    }else{
                        JOptionPane.showMessageDialog(null, "No Registrado");
                    }
                }else if(!opc_hombre.isSelected() && !opc_mujer.isSelected()){
                    JOptionPane.showMessageDialog(null, "No se ha seleccionado genero", null, JOptionPane.WARNING_MESSAGE);
                }else{
                    JOptionPane.showMessageDialog(null, "Selecciona un solo genero", null, JOptionPane.WARNING_MESSAGE);
                }
            }else{
                JOptionPane.showMessageDialog(null, "Debe llenar todos los campos", "Alerta!", JOptionPane.WARNING_MESSAGE);
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "Caracteres invalidos!", null, JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_registrar_administradorActionPerformed

