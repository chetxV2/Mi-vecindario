    public int registrar(){
        int resultado = 0;
        nombre = nombre_admin.getText();
        apellidoP = apellidoP_admin.getText();
        apellidoM = apellidoM_admin.getText();
        calle = calle_admin.getText();
        contraseña = pass_admin.getText();
        edad = Integer.parseInt(edad_admin.getText());
        telefono = Integer.parseInt(telefono_admin.getText());
        ocupantes = Integer.parseInt(ocup_admin.getText());
        vehiculos = Integer.parseInt(veh_admin.getText());
        vivienda = Integer.parseInt(casaNo_admin.getText());
        
        String sql="INSERT INTO USUARIOS VALUES("+id_admin.getText()+",'"+nombre+"', '"+apellidoP+"', '"+ apellidoM+"', "+edad+", '"+genero+"',"
                                                 +telefono+","+ocupantes+", "+vehiculos+" , '"+calle+"' , "+vivienda+", '"+contraseña+"', "+tipo+")";
        
        Connection conect = null;
        try {
            conect = DriverManager.getConnection("jdbc:oracle:thin:@localhost:1521:XE", "isw", "isw");
            Statement st = conect.createStatement();
            ResultSet rs = st.executeQuery(sql);
            if(rs.next()){
                resultado=1;
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex, "Error de conexión", JOptionPane.ERROR_MESSAGE);
        }finally{
            try {
                conect.close();
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(null, ex, "Error de desconexión", JOptionPane.ERROR_MESSAGE);
            }
        }

        return resultado;
        
    }
